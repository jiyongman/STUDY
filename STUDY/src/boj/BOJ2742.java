package boj;
import java.util.*;

public class BOJ2742 { // 기찍 N

	public static void main(String[] args) {
        
		Scanner sc = new Scanner(System.in);
        int N = sc.nextInt();
        
        for(int i = N; i > 0; i--) {
            System.out.println(i);
        }
        
        sc.close();
    }
}